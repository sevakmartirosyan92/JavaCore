package homework.chapter5;

public class SampleSwitch {
    public static void main(String[] args) {
        for (int i = 0; i < 7; i++) {
            switch (i) {
                case 0:
                    System.out.println("Պողոս");
                    break;
                case 1:
                    System.out.println("Պողոս 1");
                    break;
                case 2:
                    System.out.println("Պողոս 2");
                    break;
                default:
                    System.out.println("Պողոս Պողոսյան");
            }

        }
    }
}
